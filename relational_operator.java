//2.write a java programe for all relational operators in  a single programe.
package company;
public class relational_operator {

    public static void main (String args[])
    {
        int a = 10;
        int b = 5;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
